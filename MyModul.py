def Summa(arv1:float,arv2:float,arv3=5.0)->float:
    """Funktsioon tagastab kolme arvu summa float
       formaadis. Kolmas arv vaikimisi võrdub 5.0.
       param float arv1: Esimene arv, mis sisestab kasutaja
       param float arv2: Teine arv, mis sisestab kasutaja
       param float arv3: Kolmas arv, mis sisestab kasutaja või
       vaikimisi =5.0
       rtype:float
    """
    s=arv1+arv2+arv3
    return s
